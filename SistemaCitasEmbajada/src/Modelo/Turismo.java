package Modelo;


public class Turismo extends Visa{

	
	private static final long serialVersionUID = 1L;
	//-----------------------------------------------------------------------------------------------------------------------------------------------
	//ATRIBUTOS
	//-----------------------------------------------------------------------------------------------------------------------------------------------
	/**
	 * Los días que se piensan estar en el viaje de turismo
	 */
	private int diasEstadia;
	//-----------------------------------------------------------------------------------------------------------------------------------------------
	//CONSTRUCTOR
	//-----------------------------------------------------------------------------------------------------------------------------------------------
	/**
	 * Crea una visa de tipo Turismo para relacionarla con una solicitud determinada
	 * @param valor
	 * @param diasEstadia
	 */
	public Turismo(double valor, int diasEstadia) {
		super(valor);
		this.diasEstadia = diasEstadia;
	}
	//-----------------------------------------------------------------------------------------------------------------------------------------------
	//MÉTODOS
	//-----------------------------------------------------------------------------------------------------------------------------------------------


	//-----------------------------------------------------------------------------------------------------------------------------------------------
	//GETTERS - SETTERS
	//-----------------------------------------------------------------------------------------------------------------------------------------------
	public int getDiasEstadia() {
		return diasEstadia;
	}
	public void setDiasEstadia(int diasEstadia) {
		this.diasEstadia = diasEstadia;
	}

}
